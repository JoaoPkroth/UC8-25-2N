    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (idSelecionado ==0) {
            JOptionPane.showMessageDialog(
                    null,
                    "Selecione um Aluno,");

        return;
}

    int resposta = 
            JOptionPane.showConfirmDialog(
                        null,
                        "Deseja Excluir?",
                        "Confirmação",
                        JOptionPane.YES_NO_OPTION);

                        if (resposta == JOptionPane.YES_OPTION){
                           for (Alunos aluno : 
                                    BancoSimulado.alunos){
                               
                               if(aluno.getId()
                                       == idSelecionado) {
                               
                                   BancoSimulado.alunos
                                           .remove(aluno);
                                   
                                   JOptionPane.showMessageDialog(
                                                null,
                                                "Aluno Removido!");
                                   
                                   carregarAluno();
                                   limparCampos();
                                   
                                   idSelecionado = 0;
                                   
                                   break;
       }
      }
     }
    }//GEN-LAST:event_btnExcluirActionPerformed
  
    private void tabelaAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaAlunosMouseClicked
        int linha = 
                tabelaAlunos.getSelectedRow();
        if (linha >= 0) {
            idSelecionado = Integer.parseInt(

            tabelaAlunos
            .getValueAt(linha, 0)
            .toString());
            
            campoNome.setText(
            tabelaAlunos
            .getValueAt(linha, 1)
            .toString());
            
            campoTurma.setText(
            tabelaAlunos
            .getValueAt(linha, 2)
            .toString());
            
            campoEmail.setText(
            tabelaAlunos
            .getValueAt(linha, 3)
            .toString());
            
        }
    }//GEN-LAST:event_tabelaAlunosMouseClicked

