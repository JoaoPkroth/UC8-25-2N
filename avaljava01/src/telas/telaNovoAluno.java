    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (campoNome.getText().isEmpty()
            || campoTurma.getText().isEmpty()
            || campoEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(
                    null,
                    "Preencha todos os campos.");
            
            return;
        }
        
        Alunos aluno = new Alunos();
        
        aluno.setId(BancoSimulado.idAluno);
        aluno.setNome(campoNome.getText());
        aluno.setTurma(campoTurma.getText());
        aluno.setEmail(campoEmail.getText());
        
        BancoSimulado.alunos.add(aluno);
        
        BancoSimulado.idAluno++;
        
        JOptionPane.showMessageDialog(
                null,
                "Aluno Cadastrado com Sucesso!");
        
        carregarAluno();
        limparCampos();
        
    }//GEN-LAST:event_btnSalvarActionPerformed
  
